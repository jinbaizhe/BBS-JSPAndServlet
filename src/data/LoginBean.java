package data;



public class LoginBean {
	
	
	String logname;
	int id;
	String type;
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getLogname() {
		return logname;
	}

	public void setLogname(String logname) {
		this.logname = logname;
	}
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}